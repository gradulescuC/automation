package Method_exercises_10;

public class ArieDreptunghi {
    // facem o metoda care sa fiseze aria unui dreptunghi
    public static int ariaDreptunghi(int length , int width){
        int arie = length * width;
        return arie;
    }
    public static void main(String[] args){
        System.out.println(ariaDreptunghi(95, 105));

    }
}
